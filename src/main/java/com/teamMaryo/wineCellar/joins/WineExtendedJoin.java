package com.teamMaryo.wineCellar.joins;

public class WineExtendedJoin{
    Long wineId;
    String wineName;
    String wineDescription;
    Long quantity;
    float price;
    String location;
    Long year;
    Long rating;
    Long typeId;
    String typeName;
    String typeDescription;
    Long originId;
    String originName;
    String originDescription;
    Long userId;
    
    public WineExtendedJoin() {
    }

    public Long getWineId() {
        return wineId;
    }
    public void setWineId(Long wineId) {
        this.wineId = wineId;
    }
    public String getWineName() {
        return wineName;
    }
    public void setWineName(String wineName) {
        this.wineName = wineName;
    }
    public String getWineDescription() {
        return wineDescription;
    }
    public void setWineDescription(String wineDescription) {
        this.wineDescription = wineDescription;
    }
    public Long getQuantity() {
        return quantity;
    }
    public void setQuantity(Long quantity) {
        this.quantity = quantity;
    }
    public float getPrice() {
        return price;
    }
    public void setPrice(float price) {
        this.price = price;
    }
    public String getLocation() {
        return location;
    }
    public void setLocation(String location) {
        this.location = location;
    }
    public Long getYear() {
        return year;
    }
    public void setYear(Long year) {
        this.year = year;
    }
    public Long getRating() {
        return rating;
    }
    public void setRating(Long rating) {
        this.rating = rating;
    }
    public Long getTypeId() {
        return typeId;
    }
    public void setTypeId(Long typeId) {
        this.typeId = typeId;
    }
    public String getTypeName() {
        return typeName;
    }
    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }
    public String getTypeDescription() {
        return typeDescription;
    }
    public void setTypeDescription(String typeDescription) {
        this.typeDescription = typeDescription;
    }
    public Long getOriginId() {
        return originId;
    }
    public void setOriginId(Long originId) {
        this.originId = originId;
    }
    public String getOriginName() {
        return originName;
    }
    public void setOriginName(String originName) {
        this.originName = originName;
    }
    public String getOriginDescription() {
        return originDescription;
    }
    public void setOriginDescription(String originDescription) {
        this.originDescription = originDescription;
    }
    public Long getUserId() {
        return userId;
    }
    public void setUserId(Long userId) {
        this.userId = userId;
    }

}